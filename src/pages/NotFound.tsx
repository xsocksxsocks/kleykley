import { useLocation, Link } from "react-router-dom";
import { useEffect } from "react";
import { logError } from "@/lib/errorLogger";

const NotFound = () => {
  const location = useLocation();
  const isPortalRoute = location.pathname.startsWith('/portal');

  useEffect(() => {
    logError("NotFound", `User attempted to access non-existent route: ${location.pathname}`);
  }, [location.pathname]);

  return (
    <div className="flex min-h-screen items-center justify-center bg-muted">
      <div className="text-center">
        <h1 className="mb-4 text-4xl font-bold">404</h1>
        <p className="mb-4 text-xl text-muted-foreground">
          {isPortalRoute ? 'Seite nicht gefunden' : 'Oops! Page not found'}
        </p>
        <Link 
          to={isPortalRoute ? '/portal' : '/'} 
          className="text-primary underline hover:text-primary/90"
        >
          {isPortalRoute ? 'Zurück zum Portal' : 'Zurück zur Startseite'}
        </Link>
      </div>
    </div>
  );
};

export default NotFound;
